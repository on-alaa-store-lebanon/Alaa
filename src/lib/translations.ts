export function getTranslation(lang: Language, key: keyof typeof translations.en): string {
  return translations[lang]?.[key] || translations["en"][key] || String(key);
}

export function getProductField(lang: Language, p: any, field: "name" | "desc" | "category"): string {
  if (lang === "ar") {
    if (field === "name") return p.nameAr || p.name;
    if (field === "desc") return p.descAr || p.desc;
    if (field === "category") return p.categoryAr || p.category;
  }
  return p[field];
}

export function getStoreField(lang: Language, settings: any, field: "title" | "description"): string {
  if (lang === "ar") {
    if (field === "title") return settings.titleAr || settings.title;
    if (field === "description") return settings.descriptionAr || settings.description;
  }
  return settings[field];
}
